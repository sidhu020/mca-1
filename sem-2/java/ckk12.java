class ckkprg012
{
	public static void main(String args[])
	{
		int a[][]={{1,1,1,1,1},{3,3,3,3,3,},{5,5,5,5,5}};
		for(int i[]:a)
		{
			for(int j:i)
				System.out.print(j);
			System.out.println();
		}
	}
}	